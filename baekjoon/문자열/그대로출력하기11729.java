package 문자열;


import java.io.*;
import java.util.*;


public class 그대로출력하기11729 {

	public static void main(String[] args) {
		
		Scanner sc = new Scanner(System.in);
		
		while(sc.hasNext()) {							//입력이 존재하면 계속 받는다.
			System.out.println(sc.nextLine());			//nextLine을 통해 한줄전체를 입력받아 출력한다.
		}
		
	}
}